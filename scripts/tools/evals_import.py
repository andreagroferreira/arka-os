#!/usr/bin/env python3
"""Import the marketing-skills eval corpus into config/evals/<dept>.yaml.

Converts the 45 upstream ``evals/evals.json`` files from
coreyhaines31/marketingskills (MIT) into the ArkaOS EvalTask schema
(``core/evals/schema.py``): one YAML list per department, appended after
each department's hand-written seed tasks.

Mapping per case:
  id                  = "<dept>-<arkaos-slug>-NN"   (kebab, unique)
  department          = the owning ArkaOS department
  prompt              = upstream prompt verbatim
  expected_properties = upstream assertions[] (the verifiable checklist)
  rubric              = upstream expected_output (free-text judging guidance)
  tags                = [<arkaos-slug>, "imported"]

Deterministic and idempotent: re-running replaces the ``imported`` block
(everything tagged ``imported``) and preserves the seed tasks above it.
Third-party attribution: docs/THIRD-PARTY-NOTICES.md + skills-provenance.

Usage:
  arka-py scripts/tools/evals_import.py --source <clone>/skills [--check]
"""

